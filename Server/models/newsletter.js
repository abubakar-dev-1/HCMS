import mongoose from 'mongoose';
import { Schema } from 'mongoose';

const newsletterSchema = new Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    }
});

const Newsletter = mongoose.model('Newsletter', newsletterSchema);
export default Newsletter;